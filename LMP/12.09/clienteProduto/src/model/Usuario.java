package model;

/**
 *
 * @author aluno
 */
public class Usuario {
    private String nome;
    private int cpf;
    private int idade;
    
    public Usuario(String nome, int cpf, int idade){
        this.idade = idade;
        this.nome = nome;
        this.cpf = cpf;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getCpf() {
        return cpf;
    }

    public void setCpf(int cpf) {
        this.cpf = cpf;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }
    
    
}
